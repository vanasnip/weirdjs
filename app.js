// Coercion
// converting a value from one type to another

// happens quite often in JS because its 
// dynamically typed

var a = 1 + 2;
console.log(a);

var b = 'hello ' + 'world';
console.log(b);

var c = '1' + 2; 
console.log(c); // output : 12
//this means the value 2 was coerced into a string